    public void bill()
    {
        String date=txt_date.getText();
        String patientname=txt_pname.getText();
        String total = txt_total.getText();
         String pay = txt_pay.getText();
         String balance = txt_balance.getText();
        
          DefaultTableModel model = new DefaultTableModel();
        
        model = (DefaultTableModel)table.getModel();
        
         txt_bill.setText(txt_bill.getText() + "******************************************************\n");
          txt_bill.setText(txt_bill.getText() + "                   BUSH HOSPITAL                    \n");
         txt_bill.setText(txt_bill.getText() + "                         BILL                          \n");
         txt_bill.setText(txt_bill.getText() + "*******************************************************\n");
        
         //Heading
          txt_bill.setText(txt_bill.getText()  + "Date" + "\t" + "Patient Name" + "\t"+ "medicine" + "\t" + "Price" + "\t" + "Amount" + "\n"  );
           for(int i = 0; i < model.getRowCount(); i++)
          {
              
              String name = (String)model.getValueAt(i, 1);
              String price = (String)model.getValueAt(i, 2);
              String amount = (String)model.getValueAt(i, 4);
              
           txt_bill.setText(txt_bill.getText() + date + "\t"  + patientname + "\t"  + name  + "\t" + price + "\t" + amount  + "\n"  );
    
          }
          
          txt_bill.setText(txt_bill.getText() + "\n");    
          
          txt_bill.setText(txt_bill.getText() + "\t" + "\t" + "Subtotal :" + total + "\n");
          txt_bill.setText(txt_bill.getText() + "\t" + "\t" + "Pay :" + pay + "\n");
          txt_bill.setText(txt_bill.getText() + "\t" + "\t" + "Balance :" + balance + "\n");
          txt_bill.setText(txt_bill.getText() + "\n");
          txt_bill.setText(txt_bill.getText() + "*******************************************************\n");
          txt_bill.setText(txt_bill.getText() + "                       THANK YOU                       \n");
 
          
         
        
    }
    
    
    
    
    
    
    
    
    
    
    
    
    
