    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         try
        {
            DBConnection2 c=new DBConnection2();
            Connection con2=(Connection) c.newDBConnection2();

           
            PreparedStatement stmt=(PreparedStatement) con2.prepareStatement("select name,price from medicine where number=?");
           stmt.setInt(1,Integer.parseInt(txt_num.getText()));
            ResultSet rs=stmt.executeQuery();
            if(rs.next())
            {
                txt_name.setText(rs.getString(1));
                txt_price.setText(rs.getString(2));
            }
            else
            JOptionPane.showMessageDialog(null, "medicine does not exist");
          }
           catch(Exception e){
           }
         
    }//GEN-LAST:event_jButton1ActionPerformed

    
    
    
    
    
    
    
    
    
    
    
    
    
    
    
